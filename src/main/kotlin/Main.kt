fun main() {
    var a = 100
    var b = 25
    var ans = 100 * 25
    println("Multiplication: " + a + "*" + b + "=" + ans)

    a = 25
    b = 7
    ans = a + b
    println("Addition: " + a + "+" + b + "=" + ans)

    a = 34
    b = 8
    ans = a % b
    println("Modulo: " + a + "%" + b + "=" + ans)

    a = 75
    b = 3
    ans = a / b
    println("Division: " + a + "/" + b + "=" + ans)

    a = 9
    b = 14
    ans = a-b
    println("Subtraction: " + a + "-" + b + "=" + ans)
}